#!/bin/bash

cat /etc/passwd | sed 's/:x/ /' > output.txt

awk 'BEGIN {
    FS=":";
    
    print "| \033[34mUsername\033[0m           | \033[34mUserID\033[0m | \033[34mGroupID\033[0m | \033[34mHome\033[0m                      | \033[34mShell\033[0m                           |";

    print "_______________________________________________________________________________________________________";
           
}
    
{
    printf("| \033[33m%-18s\033[0m |\033[35m%-8s\033[0m|\033[35m%-9s\033[0m| \033[35m%-26s\033[0m| \033[35m%-32s\033[0m|\n", $1,$2,$3,$4,$5);
}
END {
    print("_______________________________________________________________________________________________________");
}' output.txt | grep -e "/usr/sbin" 

